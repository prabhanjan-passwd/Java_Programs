/* 

Reverse Coding :

Example to identify the pattern :
Input     Output
10        5
15        8
23        12
99        50
107       54

*/
import java.util.*;
public class ReverseCoding {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int num = sc.nextInt();
        System.out.println((num+1)/2);
    }
}
